public class J004For
{
	public static void main(String[] args )
	{
		int i, j;


		 for (i = 5; i < 11; i++)
		{
			System.out.print(i + " ");
		}

		System.out.println();
		System.out.println();

		for (j = 1; j < 11; j++)
		{
			System.out.print(j + " ");
		}

		System.out.println();
		System.out.println();

	}

}