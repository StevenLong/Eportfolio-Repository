//Output area of a circle with radius 2 - Steven Long

public class J01AreaCircleRadius2
{
	public static void main (String[] args)
	{
		final Double PI = 3.142;
		int Radius;
		Double Area;

		Radius = 2;

		Area = PI*Radius*Radius;

		System.out.println();
		System.out.println("Area is " + Area);
		System.out.println();

	}

}