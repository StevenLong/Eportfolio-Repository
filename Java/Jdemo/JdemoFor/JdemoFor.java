// Demo for Repetition statement

public class JdemoFor
{
	public static void main(String[] args )
	{
		int i;

		// try i=5 and i=11

		for (i = 1; i < 11; i++)
		{
			System.out.print(i + " ");
		}

		System.out.println();
		System.out.println();

	} // main

} // JdemoForr